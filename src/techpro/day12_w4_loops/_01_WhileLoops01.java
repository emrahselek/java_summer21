package techpro.day12_w4_loops;

public class _01_WhileLoops01 {

	public static void main(String[] args) {

		// FOR LOOP
		for(int i=0; i<4; i++) {
			System.out.println("hi");
		}

		System.out.println("-------");


		// WHILE LOOP
		int i=1;
		while(i<5) {
			System.out.println("emrah");
			i++;
		}
		
	}

}
