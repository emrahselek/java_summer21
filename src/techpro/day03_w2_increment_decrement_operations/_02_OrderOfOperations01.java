package techpro.day03_w2_increment_decrement_operations;

public class _02_OrderOfOperations01 {

	public static void main(String[] args) {
		
		/*
		 Order Of Operations
		 1- ()
		 2- Exponents
		 3- do multipilication and division from left to right
		 4- do addition and subtsrcation from left to right
		*/
			
		// 38 / 2 - (4 + 3) * 2 = ?
		System.out.println(38 / 2 - (4 + 3) * 2);
		
		// 8+2*(14-6/2)-12=?
		System.out.println(8+2*(14-6/2)-12);
		
	}

}
