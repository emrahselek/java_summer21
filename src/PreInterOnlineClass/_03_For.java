package PreInterOnlineClass;

public class _03_For {
    public static void main(String[] args) {

        for (int i=0; i<10; i++){
            System.out.println("Good Job");
        }

        // int i=0    -> first, read here
        // i<10       -> second, bu da kosulumuz. daha sonra java burayi okur. eger kosul saglaniyorsa asagidaki okunur.
        // System.out -> kosul saglandigi icin burasi okunur.
        // i++        -> en sonda burasi okunur. ve daha sonra ilk deger olan degiskenimizi artirir. i=1 oldu.
        //               Eger 1<10 kosulu saglanirsa o zaman loop devam eder.


    }
}
