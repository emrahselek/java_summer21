package day12_w4_ahmt_loops;

import java.util.Scanner;

public class X1_WhileLoops01 {

	public static void main(String[] args) {
						
		
		// FOR LOOP
		for(int i=0; i<4; i++) {
			System.out.println("hi");
		}
		
		
		
		
		System.out.println("-------");
		
		
		
		
		
		// WHILE LOOP
		int i=1;
		while(i<5) {
			System.out.println("emrah");
			i++;
		}
		
		
	
			
			
			
				
		
		
	}

}
