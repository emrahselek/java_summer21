package javaDersAnlatimi._Konular;

public class _17_Do_While {

    /*
    Do While kullanimi

    do {

        calistirilacak kodlar
    } while (Kosul )
     */

    public static void main(String[] args) {

        int sayi = 1;

        do {
            System.out.println("Merhaba");
            sayi++;
        }while (sayi < 5);

        System.out.println("Alt satir");

    }
}
