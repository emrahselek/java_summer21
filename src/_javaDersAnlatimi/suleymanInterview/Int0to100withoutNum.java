	/*
 		Type a program to print the integers from 0 to 100 without using any integer
    */

public class Int0to100withoutNum {

    public static void main(String[] args) {
        for (char i ='\u0000' ; i <'e'; i++){
            System.out.println((int)i);
        }
    }
}

